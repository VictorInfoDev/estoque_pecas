    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    Connection conexao = null;
    Statement comandoSelect = null;
    ResultSet resultado = null;
    codPedido.setEditable(false);
    qtdPedido.setEditable(false);
    if(qtdPedido.getText().equals("") || qtdPedido.getText() == null){
        qtdPedido.setText("0");
    }
    int qtd = Integer.parseInt(qtdPedido.getText());
    if(qtd == 0 || codPedido.getText().equals("") || codPedido.getText() == null){
        JOptionPane.showMessageDialog(null, "Insira os dados corretamente!");
        codPedido.setEditable(true);
        qtdPedido.setEditable(true);
    }else{
        try
        {
        conexao = ClasseConexao.Conectar();
        comandoSelect = conexao.createStatement();
        //LINHA DE BUSCA SQL
        String sqlSelect = "SELECT nome_peca, desc_peca, uni_peca, quant_peca FROM pecas WHERE cod_peca = '" + codPedido.getText() + "'";
        resultado = comandoSelect.executeQuery(sqlSelect);
        //RESULTADO DA BUSCA
        while(resultado.next())
        {
            nomePedido.setText(resultado.getString("nome_peca"));
            descPedido.setText(resultado.getString("desc_peca"));
            unidadePedido.setText(resultado.getString("Uni_peca"));
            qtdDisponivelPedido.setText(resultado.getString("quant_peca"));
        }

        }catch(SQLException erro){erro.printStackTrace();}
        finally{ClasseConexao.FecharConexao(conexao);
            try{comandoSelect.close();}
            catch(SQLException erro){erro.printStackTrace();}}
    }
    
    
    }//GEN-LAST:event_jButton7ActionPerformed

