    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        Date dataAtual = new Date();
        SimpleDateFormat formatoDia = new SimpleDateFormat("dd");
        SimpleDateFormat formatoMes = new SimpleDateFormat("MM");
        SimpleDateFormat formatoAno = new SimpleDateFormat("yyyy");
        String dataStringDia = formatoDia.format(dataAtual);
        String dataStringMes = formatoMes.format(dataAtual);
        String dataStringAno = formatoAno.format(dataAtual);
        Connection conexao = null;
        PreparedStatement comandoIn = null;
        try{
            conexao = ClasseConexao.Conectar();
            //LINHA DE INSERT, DELETE E UPDATE SQL
            String sqlIn = "INSERT into compra(cod_peca_compra, quant_peca_compra, estado_compra, dia, mes, ano) VALUES(?,?,?,?,?,?)";
            comandoIn = conexao.prepareStatement(sqlIn,Statement.RETURN_GENERATED_KEYS);

            //VALORES PARA OS CAMPOS DA LINHA SQL  
            comandoIn.setString(1, codPedido.getText());
            comandoIn.setString(2, qtdPedido.getText());
            comandoIn.setInt(3, 0);
            comandoIn.setString(4, dataStringDia);
            comandoIn.setString(5, dataStringMes);
            comandoIn.setString(6, dataStringAno);

            if(comandoIn.executeUpdate()>0)
            {
                //EXECUTA CASO A OPERACAO SEJA REALIZADA COM SUCESSO 
                JOptionPane.showMessageDialog(null, "Pedido criado!");
                codPedido.setText("");
                qtdPedido.setText("");
                nomePedido.setText("");
                descPedido.setText("");
                unidadePedido.setText("");
                qtdDisponivelPedido.setText("");
                codPedido.setEditable(true);
                qtdPedido.setEditable(true);
                

            }}catch(SQLException erro)
        {erro.printStackTrace();}
        finally{ClasseConexao.FecharConexao(conexao);
            try{comandoIn.close();}
            catch(SQLException erro){erro.printStackTrace();}}
    }//GEN-LAST:event_jButton8ActionPerformed

